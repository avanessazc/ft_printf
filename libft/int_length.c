#include "libft.h"

int		int_length(int n)
{
	int		i;
	int		nbr;

	i = 1;
	nbr = n;
	if (n == -2147483648)
	{
		return (11);
	}
	if (nbr < 0)
	{
		i++;
		nbr = nbr * -1;
	}
	while (nbr >= 10)
	{
		nbr = nbr / 10;
		i++;
	}
	return (i);
}
